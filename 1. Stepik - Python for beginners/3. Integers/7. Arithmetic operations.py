# Write a program that calculates the sum, difference, 
# and product of two integers entered from the keyboard.

# Input data format:
# The program is fed two integers, each on a separate line, as input.

# Output data format:
# The program should output the sum, difference and product of the entered numbers, 
# each on a separate line.

a = int(input())
b = int(input())
print(f'{a} + {b} = {a+b}')
print(f'{a} - {b} = {a-b}')
print(f'{a} * {b} = {a*b}')